import javax.persistence.*;

@Entity
@Table(name = "city")
public class City {
    private int id;
    private String name;

    public City(int id) {
        this.id = id;
    }

    public City() {
    }

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "id")
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Column(name = "name")
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}